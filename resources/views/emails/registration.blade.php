Dziękujemy za zainteresowanie naszym kursem.<br>
Najbliższa edycja zacznie się <b>25 maja 2017 r.</b> i będzie trwała 6 tygodni.<br>
Podczas 157-godzinnego kursu zdobędziesz solidne podstawy, które pomogą Ci rozwijać karierę webdevelopera.
Musisz jednak liczyć się z tym, że kurs to nie wszystko - czeka Cię dużo samodzielnej pracy, a kariera programisty
to nauka, która się nie kończy :-)<br>

Zajęcia prowadzone są w formie bootcampa - wraz z innymi uczestnikami przez 6 tygodni będziesz wykonywał różne zadania, zgodnie z programem
zamieszczonym na <a href="/kursy-programowania" target="_blank">tej stronie</a>.
Konieczne będzie przyniesienie własnego laptopa, który skonfigurujemy razem, na indywidualnym spotkaniu przed kursem.

Wszystkie zajęcia prowadzone będą w Krakowie w naszym biurze przy ul. Ketlinga 1. W pobliżu można zaparkować samochodem, możliwy jest też dojazd komunikacją miejską
liniami: 151, 178, 451. Budynek to domek zaadaptowany na biuro - przestronny, klimatyzowany. Do dyspozycji uczestników pozostaje wyposażona kuchnia i kilka łazienek.
